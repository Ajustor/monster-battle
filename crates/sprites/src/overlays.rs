//! Overlays de type secondaire.
//!
//! Chaque élément a des motifs décoratifs (flammes, gouttelettes, feuilles, etc.)
//! qui s'affichent dans les zones transparentes du sprite de base lorsqu'un monstre
//! possède un type secondaire.
//!
//! Convention des caractères :
//! - `'m'` = couleur principale du type secondaire
//! - `'d'` = couleur foncée du type secondaire
//! - `'a'` = couleur accent du type secondaire
//! - `'.'` = pas d'overlay (garder le pixel de base)
//!
//! Les overlays grandissent avec l'âge du monstre :
//! - **Baby** : 2–4 pixels, marque subtile
//! - **Young** : 5–8 pixels, effets modérés
//! - **Adult/Old** : 10–15 pixels, décoration complète

